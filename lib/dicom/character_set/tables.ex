  # ISO 8859-5 (Cyrillic) — 0xA0-0xFF maps to U+00A0, U+0401-U+045F
  @iso8859_5 (for byte <- 0xA1..0xFF, into: %{} do
                codepoint =
                  case byte do
                    0xA0 -> 0x00A0
                    0xAD -> 0x00AD
                    0xF0 -> 0x2116
                    0xFD -> 0x00A7
                    0xFE -> 0x045E
                    0xFF -> 0x045F
                    b when b in 0xA1..0xAC -> 0x0401 + (b - 0xA1)
                    b when b in 0xAE..0xEF -> 0x040E + (b - 0xAE)
                    b when b in 0xF1..0xFC -> 0x0451 + (b - 0xF1)
                    _ -> byte
                  end

                {byte, codepoint}
              end)

